#include <iostream>
#include <cmath>

using namespace std;

int main()
{
  int a;
  int b;
  int c;

  if (a + b == c)
  {
    cout << "+" << endl;
  }

  if (a - b == c)
  {
    cout << "-" << endl;
  }

  if (a * b == c)
  {
    cout << "*" << endl;
  }

  if (a / b == c)
  {
    cout << "/" << endl;
  }

  if (pow(a, b) == c)
  {
    cout << "**" << endl;
  }
}